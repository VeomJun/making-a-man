import turtle as t

t.speed('slowest')
t.shape('classic')
t.pensize(5)
t.up()
t.sety(50)
t.down()
t.color('green','red')
t.begin_fill()
t.circle(50)
t.end_fill()
t.seth(-90)
t.fd(180)
t.seth(-45)
t.fd(70)
t.back(70)
t.seth(-135)
t.fd(70)
t.up()
t.goto(100,20)
t.down()
t.seth(180)
t.fd(200)
t.done()